package com.hc360.mobile.webservice.pojo;

public class MobileMainIndInfo {
	
	private String name; // 行业名称
	
	private String code; // 行业代码
	
	private String imgurlon; // 选中的图片
	
	private String imgurloff; // 未选中的图片
	
	private String iconurl; // 行业图标
	
	private String desc;  // 行业描述
	
	private String  mimgurl; // 行业专区的图片
	
	private String type; // 1 采购通 2 M站 3 微商城
	
	private String kind; // 1 定制行业 2 行业专区

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getImgurlon() {
		return imgurlon;
	}

	public void setImgurlon(String imgurlon) {
		this.imgurlon = imgurlon;
	}

	public String getImgurloff() {
		return imgurloff;
	}

	public void setImgurloff(String imgurloff) {
		this.imgurloff = imgurloff;
	}

	public String getIconurl() {
		return iconurl;
	}

	public void setIconurl(String iconurl) {
		this.iconurl = iconurl;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getMimgurl() {
		return mimgurl;
	}

	public void setMimgurl(String mimgurl) {
		this.mimgurl = mimgurl;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}
	
	
	

}
